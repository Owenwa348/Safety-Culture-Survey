async function ใส่ข้อมูลพื้นฐานตามบริษัท(บริษัท) {
    console.log('📊 ตอนที่ 2: กำลังใส่ข้อมูลพื้นฐาน (แยกตามบริษัท)');
    console.log('----------------------------------------');

    const รายการตำแหน่ง = [
        'ผู้บริหารระดับสูง / ผู้จัดการส่วน',
        'ผู้จัดการแผนก / ผู้จัดการ',
        'พนักงานอาวุโส',
        'พนักงาน',
        'ผู้รับเหมาประจำ'
    ];

    const รายการสายงาน = ['CEO', 'REP', 'COO', 'CFO', 'SSE', 'PSE', 'CME'];

    const รายการกลุ่มงาน = [
        'หน่วยงานเดินเครื่อง (Operation)',
        'หน่วยงานบำรุงรักษา (Maintenance)',
        'หน่วยงานวิศวกรรม (Engineering)',
        'หน่วยงานสนับสนุน (Supporting functions)'
    ];

    const รายการอายุงาน = [
        '0-3 ปี',
        '3 ปีขึ้นไป แต่ไม่เกิน 5 ปี',
        '5 ปีขึ้นไป แต่ไม่เกิน 10 ปี',
        '10 ปีขึ้นไป แต่ไม่เกิน 15 ปี',
        'มากกว่า 15 ปีขึ้นไป'
    ];

    for (const บ of บริษัท) {
        const companyName = บ.ชื่อ || บ.name || 'Unknown Company';
        console.log(`\n  🏢 สร้างข้อมูลพื้นฐานสำหรับ: ${companyName} (id=${บ.id})`);

        for (const ชื่อของตำแหน่ง of รายการตำแหน่ง) {
            const มีแล้ว = await prisma.position.findFirst({ where: { name: ชื่อของตำแหน่ง, companyId: บ.id } });
            if (!มีแล้ว) await prisma.position.create({ data: { name: ชื่อของตำแหน่ง, companyId: บ.id } });
        }
        console.log(`    ✅ ตำแหน่งงาน: ${รายการตำแหน่ง.length} รายการ`);

        for (const ชื่อของสายงาน of รายการสายงาน) {
            const มีแล้ว = await prisma.department.findFirst({ where: { name: ชื่อของสายงาน, companyId: บ.id } });
            if (!มีแล้ว) await prisma.department.create({ data: { name: ชื่อของสายงาน, companyId: บ.id } });
        }
        console.log(`    ✅ สายงาน: ${รายการสายงาน.length} รายการ`);

        for (const ชื่อของกลุ่มงาน of รายการกลุ่มงาน) {
            const มีแล้ว = await prisma.work_group.findFirst({ where: { name: ชื่อของกลุ่มงาน, companyId: บ.id } });
            if (!มีแล้ว) await prisma.work_group.create({ data: { name: ชื่อของกลุ่มงาน, companyId: บ.id } });
        }
        console.log(`    ✅ กลุ่มงาน: ${รายการกลุ่มงาน.length} รายการ`);

        for (const ชื่อของอายุงาน of รายการอายุงาน) {
            const มีแล้ว = await prisma.experience.findFirst({ where: { name: ชื่อของอายุงาน, companyId: บ.id } });
            if (!มีแล้ว) await prisma.experience.create({ data: { name: ชื่อของอายุงาน, companyId: บ.id } });
        }
        console.log(`    ✅ อายุงาน: ${รายการอายุงาน.length} รายการ`);

        // ============================================================
        // สร้างหมวดหมู่และคำถามเป็นข้อมูลพื้นฐานสำหรับบริษัทนี้
        // ============================================================
        const หมวดหมู่ทั้งหมด = getข้อมูลหมวดหมู่();
        let ลำดับ = 1;

        for (let catIndex = 0; catIndex < หมวดหมู่ทั้งหมด.length; catIndex++) {
            const หมวด = หมวดหมู่ทั้งหมด[catIndex];
            const มีหมวด = await prisma.category.findFirst({ where: { name: หมวด.name, companyId: บ.id } });

            if (!มีหมวด) {
                await prisma.category.create({
                    data: {
                        name: หมวด.name,
                        companyId: บ.id,
                        order: catIndex,
                        questions: {
                            create: หมวด.questions.map((ข้อความ, idx) => ({
                                text: typeof ข้อความ === 'string' ? ข้อความ : ข้อความ.text,
                                order: ลำดับ + idx,
                                options: {
                                    create: สร้างตัวเลือกคำตอบ(ข้อความ)
                                }
                            }))
                        }
                    }
                });
                console.log(`    ✅ สร้างหมวด: ${หมวด.name} (${หมวด.questions.length} ข้อ)`);
            } else {
                console.log(`    📌 หมวดมีอยู่แล้ว: ${หมวด.name}`);
            }
            ลำดับ += หมวด.questions.length;
        }
    }

    console.log('\n✅ ใส่ข้อมูลพื้นฐานเรียบร้อย\n');
}

