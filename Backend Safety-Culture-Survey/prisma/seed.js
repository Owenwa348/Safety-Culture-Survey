    console.log(`     • คะแนน 4 (มาก): ${สถิติExpectedScore['4']} ครั้ง (~35%)`);
    console.log(`     • คะแนน 3 (ปานกลาง): ${สถิติExpectedScore['3']} ครั้ง (~10%)`);
    console.log('  ✅ ใส่คะแนนประเมินเรียบร้อย\n');
